#include "libftprintf.h"
#define CHECK(i) i = 0 ? 0 : i

void	ptf_indic_cplt(char c, t_cur_flag *a)
{
	a->i1 = CHECK(a->i1);
	a->i2 = CHECK(a->i2);
	a->i3 = CHECK(a->i3);
	a->i4 = CHECK(a->i4);
	if (c == '+')
		a->i1 = '+';
	if (c == '-')
		a->i2 = '-';
	if (c == '#')
		a->i3 = '#';
	if (c == ' ')
		a->i4 = ' ';
}
