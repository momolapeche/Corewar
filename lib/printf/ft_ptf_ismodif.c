#include "libftprintf.h"

int	ptf_ismodif(char c)
{
	if (c == 'h')
		return (1);
	if (c == 'l')
		return (1);
	if (c == 'j')
		return (1);
	if (c == 'z')
		return (1);
	if (c == 'L')
		return (1);
	return (0);
}
