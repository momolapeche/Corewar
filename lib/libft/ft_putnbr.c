#include "libft.h"

void	ft_putnbr(int i)
{
	unsigned int p;

	if (i < 0)
		ft_putchar('-');
	p = i < 0 ? -i : i;
	if (p > 9)
	{
		ft_putnbr(p / 10);
		ft_putnbr(p % 10);
	}
	else
		ft_putchar(p + '0');
}
