#include "libft.h"

int	ft_intlen(int a)
{
	int size;

	size = 0;
	if (a == 0)
		size++;
	if (a < 0)
	{
		size++;
		a = -a;
	}
	while (a > 0)
	{
		a /= 10;
		size++;
	}
	return (size);
}
